Cypress.Commands.add('saveTwoPieces', () => {
  cy.get(":nth-child(1) > .details-container > .fave-container > div > .fave-icon")
    .click()
    .get(":nth-child(2) > .details-container > .fave-container > div > .fave-icon")
    .click()
    .get(".saved-art")
    .click()
});
